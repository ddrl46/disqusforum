$(document).ready(function() {
	var paramsraw = $.param.querystring();
	if (paramsraw)
	{
		// we got parameters, let's show the topic!
		
		var params = $.deparam(Base64.decode(paramsraw));
		
		disqus_identifier = paramsraw;
		disqus_url = disqus_url_base + paramsraw;
		disqus_title = params.title;
		
		$("#thread_title").text(params.title);
		
		window.disqus_no_style = true;
		$("#disqus_thread_javascript").html('').get(0).appendChild(disqusembed);
		
		$("#loading").hide();
		$("#thread").show();
	}
	else
	{
		// organise the topics (here be dragons)
		var threads = [];
		var threadsstr = '';
		var formatchanged = false;
		
		if ($("#threadlist li.dsq-widget-item").length)
		{
			$("#threadlist li.dsq-widget-item").each(function() {
				var meta = $(this).find(".dsq-widget-meta").text().split(' ');
				if (meta[1] == 'comment' || meta[1] == 'comments')
				{
					var comments = meta[0];
				}
				else
				{
					formatchanged = true;
				}
				if (meta[5] == 'ago')
				{
					var ago = meta[3] + ' ' + meta[4] + ' ' + meta[5];
				}
				else
				{
					formatchanged = true;
				}
				
				threads.push({
					title: $(this).find(".dsq-widget-thread").text(),
					link: $(this).find(".dsq-widget-thread").attr('href'),
					comments: !formatchanged ? comments : '--',
					ago: !formatchanged ? ago : '--'
				});
			});
		}
		
		if (formatchanged)
		{
			alert('Warning! DISQUS\'s return format for the topics has changed. Expect errors!');
		}
		
		// format the table
		
		threadsstr += '<table style="width:100%;">';
		
		threadsstr += '<thead><tr><th>Title</th><th style="width:80px;">Comments</th><th style="width:112px;">Last activity</th></thead>';
		
		threadsstr += '<tbody>';
		
		$.each(threads, function(i,v) {
			threadsstr += '<tr><td><a href="' + v.link + '">' + v.title + '</a></td><td class="comments"><a href="' + v.link + '#disqus_thread">' + v.comments + '</a></td><td>' + v.ago + '</td></tr>';
		});
		
		threadsstr += '</tbody>';
		
		threadsstr += '</table>';
		
		$("#threadlist .dsq-widget-list").before(threadsstr).hide();
		
		// get the proper comment count
		var query = '?';
		$("#threadlist a").each(function(i) {
			if ($(this).attr('href').indexOf('#disqus_thread') >= 0)
			{
				query += 'url' + i + '=' + encodeURIComponent($(this).attr('href')) + '&';
			}
		});
		$.getScript('http://disqus.com/forums/a2hforumexperiment/get_num_replies.js' + query, function() {
			$(".comments").each(function() {
				$(this).text($(this).text().replace(/[^0-9]/g,''));
			});
			
			// and now we're done!
			$("#loading").hide();
			$("#home").show();
		});
	}
	
	// adding a new thread
	$("#new_thread").click(function() {
		$("#new_thread_form").slideDown().submit(function(e) {
			var workingmsg = $(workingmsg_template);
			$(this).append(workingmsg);
			
			disqus_identifier = Base64.encode($.param({
				uid: hex_sha256(Date().toString()).substring(0, 8),
				title: $("#new_thread_title").attr('value')
			}));
			
			disqus_url = disqus_url_base + disqus_identifier;
			disqus_title = $("#new_thread_title").attr('value');
			
			// http://www.pleaselistencarefully.com/2009/10/how-to-load-disqus-comments.html
			window.disqus_no_style = true;
			$("#disqus_thread_javascript").html('').get(0).appendChild(disqusembed);
			
			disqusembed.onload = function() { location.href = location.href + '?' + disqus_identifier; }
			
			$("#home").hide();
			$("#loading").show();
			
			return false;
		});
	});
});




/**
*
*  Base64 encode / decode
*  http://www.webtoolkit.info/
*
**/
 
var Base64 = {
 
	// private property
	_keyStr : "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/=",
 
	// public method for encoding
	encode : function (input) {
		var output = "";
		var chr1, chr2, chr3, enc1, enc2, enc3, enc4;
		var i = 0;
 
		input = Base64._utf8_encode(input);
 
		while (i < input.length) {
 
			chr1 = input.charCodeAt(i++);
			chr2 = input.charCodeAt(i++);
			chr3 = input.charCodeAt(i++);
 
			enc1 = chr1 >> 2;
			enc2 = ((chr1 & 3) << 4) | (chr2 >> 4);
			enc3 = ((chr2 & 15) << 2) | (chr3 >> 6);
			enc4 = chr3 & 63;
 
			if (isNaN(chr2)) {
				enc3 = enc4 = 64;
			} else if (isNaN(chr3)) {
				enc4 = 64;
			}
 
			output = output +
			this._keyStr.charAt(enc1) + this._keyStr.charAt(enc2) +
			this._keyStr.charAt(enc3) + this._keyStr.charAt(enc4);
 
		}
 
		return output;
	},
 
	// public method for decoding
	decode : function (input) {
		var output = "";
		var chr1, chr2, chr3;
		var enc1, enc2, enc3, enc4;
		var i = 0;
 
		input = input.replace(/[^A-Za-z0-9\+\/\=]/g, "");
 
		while (i < input.length) {
 
			enc1 = this._keyStr.indexOf(input.charAt(i++));
			enc2 = this._keyStr.indexOf(input.charAt(i++));
			enc3 = this._keyStr.indexOf(input.charAt(i++));
			enc4 = this._keyStr.indexOf(input.charAt(i++));
 
			chr1 = (enc1 << 2) | (enc2 >> 4);
			chr2 = ((enc2 & 15) << 4) | (enc3 >> 2);
			chr3 = ((enc3 & 3) << 6) | enc4;
 
			output = output + String.fromCharCode(chr1);
 
			if (enc3 != 64) {
				output = output + String.fromCharCode(chr2);
			}
			if (enc4 != 64) {
				output = output + String.fromCharCode(chr3);
			}
 
		}
 
		output = Base64._utf8_decode(output);
 
		return output;
 
	},
 
	// private method for UTF-8 encoding
	_utf8_encode : function (string) {
		string = string.replace(/\r\n/g,"\n");
		var utftext = "";
 
		for (var n = 0; n < string.length; n++) {
 
			var c = string.charCodeAt(n);
 
			if (c < 128) {
				utftext += String.fromCharCode(c);
			}
			else if((c > 127) && (c < 2048)) {
				utftext += String.fromCharCode((c >> 6) | 192);
				utftext += String.fromCharCode((c & 63) | 128);
			}
			else {
				utftext += String.fromCharCode((c >> 12) | 224);
				utftext += String.fromCharCode(((c >> 6) & 63) | 128);
				utftext += String.fromCharCode((c & 63) | 128);
			}
 
		}
 
		return utftext;
	},
 
	// private method for UTF-8 decoding
	_utf8_decode : function (utftext) {
		var string = "";
		var i = 0;
		var c = c1 = c2 = 0;
 
		while ( i < utftext.length ) {
 
			c = utftext.charCodeAt(i);
 
			if (c < 128) {
				string += String.fromCharCode(c);
				i++;
			}
			else if((c > 191) && (c < 224)) {
				c2 = utftext.charCodeAt(i+1);
				string += String.fromCharCode(((c & 31) << 6) | (c2 & 63));
				i += 2;
			}
			else {
				c2 = utftext.charCodeAt(i+1);
				c3 = utftext.charCodeAt(i+2);
				string += String.fromCharCode(((c & 15) << 12) | ((c2 & 63) << 6) | (c3 & 63));
				i += 3;
			}
 
		}
 
		return string;
	}
 
}

